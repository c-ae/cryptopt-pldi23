Ltac size_check tp v :=
  lazymatch tp with
  | uint_t ?nbits => constr:(0 <= v < 2 ^ nbits)
  | array_t ?E ?n => constr:(len v = n /\ List.Forall (fun (e: E) => ltac:(
      let r := size_check E e in exact r)) v)
  | ?alias => let u := eval unfold alias in alias in size_check u v
  end.

